<script>
import { browser } from '$app/environment';
import { onMount } from 'svelte';
import { Chart, registerables } from 'chart.js';

export let dataArray;
export let labelsArray;
export let datasetTitle = "";
export let token = "";

Chart.register(...registerables);
let donutChartElement;

   onMount(() => {
     if (browser) {

        const chartData = {
            labels: labelsArray,
            datasets: [
            {
                label: "Total "+token,
                data: dataArray,
                backgroundColor: [
                'rgb(134, 52, 145)',
                'rgb(95, 100, 176)',
                'rgb(10, 203, 241)'
              ],
              hoverOffset: 6,
                // borderColor: ['rgb(240,255,255)'],
                // borderRadius: 7,
                // borderWidth: 2,
            },
            ],
        };
       let options = {
           plugins: {
                title: {
                  display: true,
                  text: datasetTitle
                },
                legend: {
                  display: true,
                  tooltip: true,
                },
            },
            cutout : '80%',
            responsive: true,
            maintainAspectRatio: false
          };

        Chart.defaults.color = 'rgb(240,255,255)'; // text
        Chart.defaults.borderColor = 'rgba(20,20,20,0.45)';
       new Chart(donutChartElement, {
         type: 'doughnut',
         data: chartData,
         options: options,
       });
     }
   });

</script>
<canvas bind:this={donutChartElement} />
